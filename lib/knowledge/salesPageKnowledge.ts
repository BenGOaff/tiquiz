/**
 * Build the complete knowledge block for AI system prompt.
 * Returns all knowledge concatenated, ready to inject.
 */
export function buildCopywritingKnowledge(pageType: "capture" | "sales" | "showcase"): string {
  const sections: string[] = [];

  sections.push("═══════════════════════════════════════════════════════════════");
  sections.push("TIPOTE KNOWLEDGE — COPYWRITING HAUTE CONVERSION");
  sections.push("Inspire-toi de ces frameworks ÉPROUVÉS pour créer un contenu premium.");
  sections.push("NE COPIE PAS mot pour mot — adapte au contexte de l'utilisateur.");
  sections.push("═══════════════════════════════════════════════════════════════");
  sections.push("");

  // Always include puces promesses and headlines
  sections.push(PUCES_PROMESSES_FRAMEWORKS);
  sections.push("");
  sections.push(HEADLINE_FORMULAS);
  sections.push("");

  if (pageType === "capture") {
    sections.push(CAPTURE_PAGE_BEST_PRACTICES);
  } else {
    sections.push(SALES_PAGE_FRAMEWORKS);
  }

  return sections.join("\n");
}
